export function getRelatedGalleryItems(itemId: string) {
  const item = galleryItems.find((item) => item.id === itemId);
  if (!item) return [];

  return galleryItems
    .filter(
      (relatedItem) =>
        relatedItem.category === item.category && relatedItem.id !== itemId
    )
    .slice(0, 4);
}

